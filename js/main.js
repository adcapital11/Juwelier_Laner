// Shared Cookie Helpers for filesystem compatibility
function getLanerCookie(name) {
    const nameEQ = name + "=";
    const ca = document.cookie.split(';');
    for (let i = 0; i < ca.length; i++) {
        let c = ca[i];
        while (c.charAt(0) === ' ') c = c.substring(1, c.length);
        if (c.indexOf(nameEQ) === 0) return decodeURIComponent(c.substring(nameEQ.length, c.length));
    }
    return null;
}

function setLanerCookie(name, value, days = 365) {
    const date = new Date();
    date.setTime(date.getTime() + (days * 24 * 60 * 60 * 1000));
    document.cookie = name + "=" + encodeURIComponent(value) + "; expires=" + date.toUTCString() + "; path=/";
}

// Global language switcher logic
let currentLang = "de";
try {
    const urlParams = new URLSearchParams(window.location.search);
    const urlLang = urlParams.get('lang');
    if (urlLang && translations[urlLang]) {
        currentLang = urlLang;
    } else {
        currentLang = localStorage.getItem("laner-lang") || getLanerCookie("laner-lang") || "de";
    }
} catch (e) {
    currentLang = getLanerCookie("laner-lang") || "de";
}

function setLanguage(lang) {
    if (!translations[lang]) return;
    currentLang = lang;
    
    try {
        localStorage.setItem("laner-lang", lang);
    } catch (e) {}
    setLanerCookie("laner-lang", lang);
    
    // Dynamically append lang query parameter to all local links to bypass filesystem sandbox limits
    document.querySelectorAll("a").forEach(a => {
        const href = a.getAttribute("href");
        if (href && !href.startsWith("http") && !href.startsWith("#") && !href.startsWith("tel:") && !href.startsWith("mailto:")) {
            const parts = href.split('#');
            const pathAndQuery = parts[0];
            const hash = parts[1] ? '#' + parts[1] : '';
            const basePath = pathAndQuery.split('?')[0];
            a.setAttribute("href", basePath + "?lang=" + lang + hash);
        }
    });
    
    // Update active class on switcher items
    document.querySelectorAll(".lang-switcher li").forEach(li => {
        if (li.getAttribute("data-lang") === lang) {
            li.classList.add("active");
        } else {
            li.classList.remove("active");
        }
    });

    // Update all translation nodes
    document.querySelectorAll("[data-i18n]").forEach(el => {
        const key = el.getAttribute("data-i18n");
        if (translations[lang][key]) {
            el.innerHTML = translations[lang][key];
        }
    });

    // Update inputs or placeholder targets
    document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
        const key = el.getAttribute("data-i18n-placeholder");
        if (translations[lang][key]) {
            el.setAttribute("placeholder", translations[lang][key]);
        }
    });

    // Adjust document lang attribute
    document.documentElement.setAttribute("lang", lang);
}

